#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const t_uchar	*str1;
	const t_uchar	*str2;

	str1 = (t_uchar*)s1;
	str2 = (t_uchar*)s2;
	while (n--)
	{
		if (*str1 != *str2)
			return (*str1 - *str2);
		else
		{
			str1++;
			str2++;
		}
	}
	return (0);
}
